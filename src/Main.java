public class Main {
    public static void main(String[] args) {

        int x = 10;
        do {
            System.out.println("Hello world");
        }
        while (x < 5);
        while (x < 5) {
            System.out.println("HELLO");
        }
    }
}
//  do {тело_цикла_из_набора_операторов
//  } while (условие) отличается от простого while, так как этот цикл с постусловием_ первая итерация выполняется
// не смотря на условия написанные в while



